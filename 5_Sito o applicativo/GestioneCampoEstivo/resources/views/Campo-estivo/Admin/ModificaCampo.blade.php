          <h1 style="text-align: left;">
            <span style="font-size: 1.5rem; font-weight: 700;">
              <span style="font-size: 1.875rem;">La Storia</span>
            </span>
          </h1>
          <p>
            <span style="font-size: 1.125rem;">Dal 1970, nel mese di luglio, volontari di tutte le età e disabili si incontrano per trascorrere insieme due settimane davvero speciali.&nbsp;</span>
          </p>
          <p>
            <span style="font-size: 1.125rem;">Succede ogni anno al Campo di vacanza di Olivone. A dare vita al primo Campo sono Italo e Gemma Balzaretti che in seguito creano la Fondazione Vita Serena, per dare continuità a questa iniziativa. Durante le due settimane un centinaio di volontari si intercalano per garantire ogni tipo di assistenza agli ospiti, persone invalide prevalentemente anziane.&nbsp;</span>
          </p>
          <p>
            <span style="font-size: 1.125rem;">In un così vasto gruppo di persone si possono trovare le risorse più adatte per tutti i servizi necessari alla buona riuscita di questa esperienza. I volontari si svegliano di buon mattino e mentre alcuni aiutano gli ospiti ad alzarsi, altri si preparano per servire loro la colazione. Nel frattempo mani esperte si mettono al lavoro per far trovare biancheria sempre fresca e rassettare i locali, ed in cucina fervono già i preparativi per il pranzo.&nbsp;</span>
          </p>
          <p>
            <span style="font-size: 1.125rem;">Al mattino c'è la possibilità di prendere parte alla Santa Messa, animata dalle voci e dalle chitarre dei giovani volontari. In seguito è possibile partecipare alle attività di ginnastica guidate dai fisioterapisti, mentre i più sedentari troveranno senz'altro qualcuno con cui scambiare quattro chiacchiere commentando l'attualità dei giornali.&nbsp;</span>
          </p>
          <p>
            <span style="font-size: 1.125rem;">Il pomeriggio è il momento di una passeggiata in compagnia e di diverse altre attività ricreative; dopo cena si assiste tutti insieme allo spettacolo serale. Quest’ultimo è anche un piacevole momento di scambio tra il Campo e gli abitanti della Valle, che spesso raggiungono la struttura di Gioventù e Sport per assistere alle esibizioni musicali e teatrali.&nbsp;</span>
          </p>
          <p>
            <span style="font-size: 1.125rem;">L’ultima emozione regalata dal Campo è il momento dei saluti, dove allegria e commozione si alternano sui volti di ospiti e volontari. Durante il ritorno a casa è inevitabile portarsi dietro anche un po’ di malinconia, spesso accompagnata dal desiderio di rivivere un’esperienza che a detta di molti ha del meraviglioso. Ma non c’è motivo di essere tristi: si tratta soltanto di un arrivederci!</span>
          </p>
